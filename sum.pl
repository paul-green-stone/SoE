#!/usr/bin/perl

use v5.38;

# ================================================================ #
# ---------- A simple Perl script that tries to sum up ----------- # 
# --------------------- whatever comes from ---------------------- # 
# ---------------- STDIN (numbers preferably) : ------------------ #
# ================================================================ #

my $sum = 0;

while (<>) {

    chomp ;

    $sum += $_;
}

say $sum;

